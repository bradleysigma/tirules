<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '33.' counter(item, decimal);} </style>
<p>A player who is eliminated is no longer part of the game.</p>
    <ol class="lrr">
    <li>A player is eliminated when they meet <b>all</b> of the following three conditions:</li>
    <ol>
        <li>The player has no ground forces on the game board.</li>
        <li>The player has no unit that has <sc>Production</sc>.</li>
        <li>The player does not control any planets.</li>
    </ol>
    <li>When a player becomes eliminated, all of the units, command tokens, control tokens, promissory notes, technologies, command sheets, and the faction sheet that matches that player&rsquo;s faction or color are returned to the game box, including those in their reinforcements.</li>
    <li>When a player becomes eliminated, all agenda cards they own are discarded.</li>
    <li>When a player becomes eliminated, each promissory note they have that matches another player&rsquo;s faction or color is returned to that player.</li>
    <ol><li>Promissory notes that match the eliminated player are returned to the game box, even if another player has them.</li></ol>
    <li>When a player becomes eliminated, each action card in their hand is discarded.</li>
    <li>When a player becomes eliminated, their strategy cards are returned to the common play area whether those cards have been exhausted or not.</li>
    <li>When a player becomes eliminated, their secret objectives are shuffled back into the secret objective deck whether those secret objectives have been completed or not.</li>
    <li>If the speaker becomes eliminated, the speaker token passes to the player to the speaker&rsquo;s left.</li>
    <li>If a game that started with five or more players becomes a game with four or fewer players due to elimination, the players continue to select only one strategy card during the strategy phase.</li>
    <li>When players are eliminated, faction&ndash;specific components interact with the game as follows:</li>
    <ol>
        <li>If a player becomes eliminated and the Nekro Virus&rsquo; assimilator &ldquo;X&rdquo; or assimilator &ldquo;Y&rdquo; token is placed on one of their faction technologies, that technology remains in play.</li>
        <li>If the Ghost of Creuss player becomes eliminated, their wormhole tokens remain on the game board for the remainder of the game.</li>
        <li>If the Naalu player becomes eliminated while another player has the Naalu player&rsquo;s &ldquo;0&rdquo; token, that token remains with its current player until the end of the status phase, and then it is removed from play.</li>
        <li>If the Titans of Ul player becomes eliminated while their hero or promissory note is attached to a planet, those attachments and attachment tokens remain in play for the remainder of the game.</li>
        <li>If the Mahact Gene&ndash;Sorcerers become eliminated while they have another player&rsquo;s command tokens on their faction sheet, those command tokens are returned to their respective players&rsquo; reinforcements.</li>
        <li>If the Mahact Gene&ndash;Sorcerers have an eliminated player&rsquo;s command token on their faction sheet, that command token remains in play, as does the eliminated player&rsquo;s commander, if it is unlocked.</li>
    </ol>
    <li>If a player becomes eliminated, any units they have captured are returned to the reinforcements of their original owners.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>While the Arborec flagship may produce units, it does not have the <sc>Production</sc> ability. Having it on the game board will not prevent the Arborec player from being eliminated. This applies to all similar effects in the game.</li>
    <li>An eliminated player&rsquo;s home system remains a home system for game effects.</li>
    <li>If an eliminated player had any relic fragments, they are discarded to their respective exploration discard piles. If an eliminated player had any relics, they are purged.</li>
    </ol>

