<h1>Age of Commerce</h1>
    <ol class="note">
    <li>Players must still be neighbors on the game board for the purpose of resolving game effects that depend on players being neighbors.</li>
    <ol><li>For example, the Mentak player must still be neighbors with another player on the game board to resolve their <sc>Pillage</sc> faction ability against that player.</li></ol>
    <li>The Hacan player&rsquo;s <sc>Guild Ships</sc> faction ability and <i>Trade Convoys</i> promissory note provide no benefit while playing with the <i>Age of Commerce</i> event. However, <i>Trade Convoys</i> may still be played as a component action.</li>
    <li>If the Empyrean player receives commodities from a player with their <i>Dark Pact</i> promissory note in their play area equal to that player&rsquo;s printed commodity value, then both players will gain one trade good. This need not be all of the giving player&rsquo;s current commodities.</li>
    <ol><li>If the giving player owns the <i>Dynamis Core</i> relic, they must give two commodities above their faction&rsquo;s printed commodity value to trigger the effect of <i>Dark Pact</i>.</li></ol>
    <li>When a player&rsquo;s <i>Trade Agreement</i> promissory note is played, they give only the number of commodities that they just gained due to replenishing commodities.</li>
    <li>A player may share any number of technologies in a single transaction.</li>
    </ol>

