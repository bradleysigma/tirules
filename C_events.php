<h1>Minor Factions</h1>
    <ol class="note">
    <li>During setup, if a player places a minor faction system with two planets, that player chooses which planet gets two neutral infantry and which planet gets one.</li>
    <li>During setup, if a player is dealt the Creuss reference card, they place the Creuss Gate (tile 17) in the equidistant, and place the Creuss home system (tile 51) in the common play area.</li>
    <li>During setup, if a player is dealt the Keleres reference card, then they select a home system at random from amongst the Argent, Mentak and Xxcha, excluding any that have already been placed.</li>
    <ol>
        <li>Should another player have the reference card for that faction, then they take that home system, and the player with the Keleres reference card places a different home system at random.</li>
        <li>The pool of player factions and minor factions cannot contain all four of the Argent, Keleres, Mentak and Xxcha.</li>
        <li>When a player controls each planet in this system, they take the Keleres alliance card, granting them the ability of Suffi An, the Keleres commander. They are not granted the ability of the Argent, Mentak or Xxcha commanders.</li>
    </ol>
    <li>If Mahact exists as a minor faction, the player that controls Ixth gains access to the ability of Il Na Viroset, the Mahact commander.</li>
    <li>Commander abilities of minor factions do not need to be unlocked; they are available as soon as a player takes the corresponding alliance card.</li>
    <li>When a player gains control of a minor faction planet, if it is not controlled by another player, they explore it. They choose which planet trait they explore it as.</li>
    <li>A player only loses a minor faction&rsquo;s alliance card if a single other player controls each planet in that minor faction&rsquo;s system.</li>
    <ol><li>A player will retain a minor faction&rsquo;s alliance card even if they control no planets in that minor faction&rsquo;s system, should two or more other players each control planets in that system.</li></ol>
    <li>The planets in a minor faction&rsquo;s system are not home planets, nor is the system a home system.</li>
    <ol>
        <li>The minor faction systems cannot be used to score objectives such as <i>Conquer the Weak</i> or <i>Darken the Skies</i>, but may be used to score objectives such as <i>Expand Borders</i>.</li>
        <li>The minor faction systems may be targeted by action cards such as <i>Reactor Meltdown</i>, <i>Signal Jamming</i> or <i>Uprising</i>.</li>
        <li>The minor faction systems may be elected for agendas such as <i>Colonial Redistribution</i>. They will be unaffected by agendas such as <i>New Constitution</i> or <i>Shared Research</i>.</li>
        <li>The minor faction systems may be targeted with the <i>Stellar Converter</i> relic or with the Muaat player&rsquo;s <i>Nova Seed</i> hero ability. These will not cause a player to lose an alliance card.</li>
    </ol>
    <li>All neutral infantry have a combat value of 8, and otherwise have no special abilities, regardless of which minor faction they belong to.</li>
    <li>Minor factions are not players.</li>
    <ol>
        <li>Planets with neutral infantry cannot be targeted with action cards such as <i>Plague</i> or <i>Uprising</i>.</li>
        <li>Planets with neutral infantry cannot be elected by &ldquo;elect planet&rdquo; agendas, and are unaffected by agendas such as <i>Swords to Plowshares</i>.</li>
        <li>Planets with neutral infantry cannot be used to score objectives such as <i>Make an Example of their World</i> or <i>Stake Your Claim</i>.</li>
    </ol>
    </ol>

<h1>Total War</h1>
    <ol class="note">
    <li>Commodities on planets are not treated as commodities for any game effect, other than those that appear on the <i>Total War</i> event card.</li>
    <ol>
        <li>The Mentak player cannot use their <sc>Pillage</sc> faction ability to target the commodities on planets.</li>
        <li>The <i>Salvage</i> action card cannot be used to target the commodities on planets.</li>
    </ol>
    <li>If a player gains control of a planet in another player&rsquo;s home system, without controlling at least one planet in their own home system, then any commodities on the planet they gained control of remain on that planet.</li>
    <ol>
        <li>The commodities on the planet in the other player&rsquo;s home system cannot be discarded to gain a victory point.</li>
        <li>If that player still controls that planet in another player&rsquo;s home system when they regain control of a planet in their own home system, they then move the commodities from the planet in the other player&rsquo;s home system to the planet in their own home system.</li>
    </ol>
    </ol>
