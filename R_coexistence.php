<p style="column-span: all;">There currently does not exist a Living Rules Reference section regarding coexistence.</p>

<h1>Notes</h1>
    <ol class="note">
    <li>Some game effects allow two or more players&rsquo; units to exist on a planet without triggering combat; this is coexistence.</li>
    <li>One player controls the planet; all other players are coexisting on that planet.</li>
    <ol>
        <li>If a game effect instructs a player to coexist on a planet they do not control, the original controller of that planet retains control.</li>
        <li>If a game effect instructs a player to coexist on a planet they do control, then another player will gain control of that planet (which will exhaust that planet).</li>
    </ol>
    <li>A coexisting structure is always blockaded, regardless of what ships, if any, are in the system.</li>
    <li>If additional units are added to those already coexisting, by, for example, committing additional ground forces during an invasion, or resolving an effect that allows a unit to be placed directly on a planet, the added units immediately coexist.</li>
    <li>If only one player has units on a planet that was in coexistence, the coexistence ends, and that player gains control of that planet if they were the coexisting player.</li>
    <li>When a player uses the <sc>Bombardment</sc> ability of 1 of their units against a planet in coexistence, they choose which player&rsquo;s units on that planet will have the hits assigned.</li>
    <ol>
        <li>If that player has multiple units with the <sc>Bombardment</sc> ability, they make this choice independently for each unit.</li>
        <li>If that player produces a surplus of hits above the number of units of the player they have chosen, the extra hits cannot be assigned to the units of a different player on that planet.</li>
    </ol>
    <li>A player who controls a planet may start a ground combat against a coexisting player during the invasion step of a tactical action.</li>
    <li>A player who is coexisting on a planet may start a ground combat against the player that controls that planet during the invasion step of a tactical action.</li>
    <li>A player who had no units on a planet, but commits 1 or more ground forces during the invasion step of a tactical action must start a ground combat against the player that controls that planet (unless they have an ability that would allow them to coexist).</li>
    <li>Should a player win the ground combat on a planet resulting from one of these aforementioned methods, they gain control of that planet, if they were not in control of it already. Then, they may start an additional ground combat against another coexisting player, if present, and may continue to do so until they decline to start another ground combat or there are no more coexisting players. Any coexisting players that they do not resolve a ground combat against remain coexisting.</li>
    <li>A player is considered to control a planet they are coexisting on solely when scoring an objective. For any other game ability or effect, they are not considered to control that planet.</li>
    </ol>

