<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '69.' counter(item, decimal);} </style>
<p>Each player begins the game with one unique and five generic promissory note cards that can be given to other players.</p>
    <ol class="lrr">
    <li>Each promissory note contains timing text and ability text. A player can resolve any of their promissory note cards by following the text on the card.</li>
    <ol><li>Promissory notes are <b>not</b> mandatory unless otherwise specified.</li></ol>
    <li>A player cannot play their color&rsquo;s or faction&rsquo;s promissory notes. Since the cards are only valuable to other players, promissory notes can be traded as powerful negotiation tools.</li>
    <li>Promissory notes that are returned to a player are returned after their abilities have been completely resolved.</li>
    <li>If a promissory note is returned to a player, that player may give it to other players again as part of a future transaction.</li>
    <ol><li>An unrevealed promissory note is not subject to effects in its ability text that return the card if certain conditions are met.</li></ol>
    <li>When resolving a transaction, a player can trade a maximum of one promissory note from their hand to another player, even if that card originally belonged to another player.</li>
    <ol><li>Promissory notes in the play area cannot be traded.</li></ol>
    <li>Players should keep their hands of promissory notes hidden.</li>
    <li>If a player is eliminated, all of the promissory notes that match their color or faction are returned to the game box, including those that are in play or owned by other players.</li>
    <ol><li>Other players&rsquo; promissory notes are returned to those players.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>When two players make a trade for a promissory note, the specific promissory note that was traded may be hidden from other players.</li>
    <ol>
    	<li>However, it is public information that a promissory note was traded.</li>
        <li>The <i>Alliance</i> and <i>Support for the Throne</i> promissory notes must be played immediately when received. As such, trading either of these notes cannot be kept hidden.</li>
    </ol>
    <li>A player may choose to reveal any or all of the promissory notes in their hand, if they so choose.</li>
    <li>For notes about specific generic promissory notes, see the <a href="/C_promissory_notes">promissory notes notes page</a>.</li>
    <li>For notes about a promissory note of a specific faction, see that faction&rsquo;s notes page.</li>
    </ol>

