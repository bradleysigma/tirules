<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '59.' counter(item, decimal);} </style>
<p>A nebula is an anomaly that affects movement and combat.</p>
    <ol class="lrr">
    <li>A ship can only move into a nebula if it is the active system.</li>
    <ol><li>A ship cannot move through a nebula. That is, a ship cannot move into and out of a nebula during the same movement.</li></ol>
    <li>A ship that begins the <b>Movement</b> step of a tactical action in a nebula treats its move value as &ldquo;1&rdquo; for the duration of that step.</li>
    <ol><li>Other abilities and effects can increase this number.</li></ol>
    <li>If a space combat occurs in a nebula, the defender applies +1 to each combat roll of their ships during that combat.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A player cannot retreat (or similar) into a nebula, since it would not be the active system.</li>
    <li>The +1 modifiers for the defender does not modify anti&ndash;fighter barrage, bombardment or space cannon rolls.</li>
    <li>A future rule change will place the ship movement restrictions of anomalies upon all units.</li>
    <li>A future rule change will allow ships to move into a nebula when it is not the active system.</li>
    </ol>

