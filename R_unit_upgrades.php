<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '97.' counter(item, decimal);} </style>
<p>A unit upgrade is a type of technology card.</p>
    <ol class="lrr">
    <li>Unit upgrades share a name with a unit that is printed on a player&rsquo;s faction sheet, but have a higher roman numeral. For example, a player&rsquo;s &ldquo;Carrier I&rdquo; unit is upgraded by the unit upgrade technology &ldquo;Carrier II&rdquo;.</li>
    <ol><li>The Nekro Virus player may upgrade their units with units of the same type (e.g., &ldquo;dreadnought&rdquo; or &ldquo;infantry&rdquo;) even if those unit&rsquo;s names do not match. If the Nekro Virus gains a unit upgrade technology of the same unit type as a unit upgrade technology they already have, the previous upgrade is removed, and they must use the same Valefar Assimilator token that was used to copy the previous upgrade.</li></ol>
    <li>Players place unit upgrades they gain faceup on their faction sheets, covering the unit that shares a name with that upgrade card.</li>
    <li>The white arrows next to an attribute on a faction sheet indicate that the attribute will improve when the unit is upgraded.</li>
    <li>After a player gains a unit upgrade card, each of that player&rsquo;s units that correspond to that upgrade card is treated as having the attributes and abilities printed on that upgrade card. Any previous attributes of that unit, such as the one printed on that player&rsquo;s faction sheet, are ignored.</li>
    <li>A mech unit card is not a technology.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>If a player is playing a faction with a faction&ndash;specific unit upgrade, they cannot research or otherwise gain the generic upgrade for that unit type.</li>
    <li>For notes about specific generic unit upgrades, see the <a href="/C_units">unit component notes page</a>.</li>
    <li>For notes about a unit upgrade technology of a specific faction, see that faction&rsquo;s notes.</li>
    </ol>

