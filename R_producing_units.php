<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '67.' counter(item, decimal);} </style>
<p>The primary way that a player produces new units is by resolving the <sc>Production</sc> abilities of existing units during a tactical action. However, other game effects also allow players to produce units.</p>
    <ol class="lrr">
    <li>Each unit that a player can produce has a cost value presented on its faction sheet or technology card. To produce a unit, a player must spend a number of resources equal to or greater than the cost value of the unit they are producing.</li>
    <ol>
        <li>Spent resources must come from planets or trade goods that are controlled by the player who is producing the units.</li>
        <li>Any resources spent in excess of a unit&rsquo;s cost are lost.</li>
        <li>If a player is producing multiple units at a time, that player can add the cost of each unit they are producing to create a total cost before they spend any resources.</li>
    </ol>
    <li>If the cost is accompanied by two icons &ndash; typically for fighters and infantry &ndash; a player produces two of that unit for that cost.</li>
    <ol>
        <li>Each of the two units counts toward the total number of units a player can produce.</li>
        <li>A player can choose to produce only one unit; however, they must still pay the entire cost.</li>
    </ol>
    <li>When a player produces a unit through the use of their units&rsquo; <sc>Production</sc> abilities during a tactical action, that player follows the rules of the <sc>Production</sc> ability to determine where in the active system the units can be placed.</li>
    <li>When a player produces a unit through an ability outside of the tactical action, that ability will state how many units that player can produce and where that player can place those units.</li>
    <ol>
        <li>A player cannot produce a unit on a planet they do not control.</li>
        <li>If an ability allows a player to produce a unit in a system, they may produce that unit in the space area or on a planet they control in that system.</li>
    </ol>
    <li>A player is limited by the number of units in their reinforcements.</li>
    <ol>
        <li>If a player cannot produce a unit because it is not in their reinforcements, that player can remove a unit from any system that does not contain one of their command tokens and place that unit in their reinforcements. Then, that player can produce that unit. A player can remove any number of their units in this way; however, any units that are removed must be produced immediately.</li>
        <li>When producing a fighter or infantry unit, a player can use a fighter or infantry token, as appropriate, from the supply instead of a plastic piece.</li>
    </ol>
    <li>A player cannot produce ships in a system that contains other players&rsquo; ships.</li>
    <ol><li>Ground forces can still be produced.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>Whenever a unit is produced, its cost must be paid, unless the effect that is producing the unit explicitly says otherwise.</li>
    <li>A player cannot produce one fighter and one infantry for one resource.</li>
    <li>If an effect allows a player produce a ship of a specific type, then factional variations do not prevent that player from producing that type of ship.</li>
    <li>If a player removes a unit in order to place it, because they have none of that unit type in their reinforcements, that unit is placed undamaged.</li>
    </ol>

