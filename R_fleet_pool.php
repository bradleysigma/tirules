<p>The fleet pool is an area of a player&rsquo;s command sheet.</p>
    <ol class="lrr">
    <li>The number of command tokens in a player&rsquo;s fleet pool indicates the maximum number of non&ndash;fighter ships that a player can have in a system.</li>
    <ol>
        <li>Units that are on planets or that count against a player&rsquo;s capacity do not count against that player&rsquo;s fleet pool.</li>
        <li>Units that are being transported through systems do not count against a player&rsquo;s fleet pool in those systems.</li>
    </ol>
    <li>Players place command tokens in their fleet pools with the ship silhouette faceup.</li>
    <li>If at any time the number of a player&rsquo;s non&ndash;fighter ships in a system exceeds the number of tokens in that player&rsquo;s fleet pool, they choose and remove excess ships in that system, returning those units to their reinforcements.</li>
    <li>Players do not spend command tokens from this pool unless a game effect specifically allows it.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>Fighters and ground forces do not count towards capacity during combat. As such, any number of Fighter II may be in a system during combat, regardless of capacity and fleet pool. This will usually only be caused by effects that place units in a system during combat. When combat ends however, units will need to be removed to meet capacity and fleet pool requirements.</li>
    <li>Ships may be moved into, produced in or placed in a system even if doing so would exceed the fleet pool limit. However, some of those ships must immediately be removed to satisfy the limit.</li>
    <ol><li>If some of those ships are fighters II, they may instead count towards that player&rsquo;s capacity in that system. If capacity is exceeded, fighters or ground forces must be removed to meet the limit.</li></ol>
    <li>During movement, a ship may move through a system regardless of how many ships are in that system relative to the fleet pool limit.</li>
    </ol>

