<h1>Notes</h1>
    <ol class="note">
    <li>Certain abilities, primarily breakthroughs, grant synergy between two colors of technology, allowing components of one color to be treated as the other for some game effects.</li>
    <li>When researching a technology, a player may treat each technology they own and/or each technology speciality on a planet they control that matches one color of their synergy as any color of that synergy.</li>
    <li>When scoring an objective, a player may treat each technology they own that matches one color of their synergy as any color of that synergy.</li>
    <li>These choices are made individually for each technology and/or each technology speciality.</li>
    <ol><li>For example, a player with two propulsion technologies and a propulsion/biotic synergy ability would meet the prerequisites of the Fighter II unit upgrade technology. They do not have to treat their two technologies as both propulsion or both biotic, but may pick and choose for each.</li></ol>
    <li>Each technology and/or each technology speciality can only be one color of technology at any given point in time.</li>
    <ol><li>For example, a player with just one propulsion technology and a propulsion/biotic synergy ability would fail to meet the prerequisites of the Fighter II unit upgrade technology. Their single propulsion technology may be treated as either a propulsion technology or a biotic technology, but not both at the same time.</li></ol>
    <li>A technology and/or each technology speciality may be treated as different colors of technologies at different points in time.</li>
    <ol><li>For example, say a player currently has just one biotic technology, and they are resolving the primary ability of the <i>Technology</i> strategy card to research two technologies; they have a propulsion/biotic synergy ability. They may first treat the biotic technology as a propulsion technology in order to research a technology with one propulsion prerequisite. Then, they may treat both the original biotic technology and the new propulsion technology as biotic technologies in order to research a technology with two biotic prerequisites.</li></ol>
    </ol>

