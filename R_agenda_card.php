<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '7.' counter(item, decimal);} </style>
<p>Agenda cards represent galactic laws and policies. During each agenda phase, players cast votes for specific outcomes on two agenda cards.</p>
    <ol class="lrr">
    <li>There are two types of agenda cards: laws and directives.</li>
    <li>Laws can permanently change the rules of the game.</li>
    <li>When resolving a law, if a &ldquo;For&rdquo; outcome received the most votes, or if the law requires an election, the law&rsquo;s ability becomes a permanent part of the game. Players resolve the outcome and place the agenda card either in the common play area or in a player&rsquo;s play area, as dictated by the card.</li>
    <li>If a law is in a player&rsquo;s play area as opposed to the common play area, that player owns that law.</li>
    <li>If a law is discarded from play, that law&rsquo;s ability is no longer in effect. Place that card on the top of the agenda card discard pile.</li>
    <li>If an &ldquo;Against&rdquo; outcome of a law received the most votes, players resolve the outcome and discard the agenda.</li>
    <li>Directives provide one&ndash;time game effects.</li>
    <li>When resolving a directive, players resolve the outcome that received the most votes and discard the agenda card.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>When resolving the primary ability of the <i>Politics</i> card, the active player cannot show the agenda cards they drew to any other player.</li>
    <li>If the outcome of an agenda affects multiple players, it will do so in speaker order.</li>
    <li>For notes about specific agenda cards, see the <a href="/C_agendas">agenda card component notes page</a>.</li>
    </ol>

