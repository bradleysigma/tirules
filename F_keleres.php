<h1>Law&rsquo;s Order</h1>
    <ol class="note">
    <li>The blanked laws come back into effect at the end of the turn.</li>
    <li>If this ability is used, all laws are blanked. The Keleres player cannot choose for some laws to remain in effect.</li>
    <li>If the Keleres player owns the <i>Political Censure</i> law, they will not lose a victory point when it is blanked, and they may use action cards during this turn.</li>
    <ol><li>If the Keleres player then uses the <i>Repeal Law</i> action card, targeting the <i>Political Censure</i> law, then it will be discarded without the Keleres player losing the victory point.</li></ol>
    <li>The Keleres player and all of the other players treat all laws as blank for the turn.</li>
    </ol>

<h1>Keleres Rider <sub>(Promissory Note)</sub></h1>
    <ol class="note">
    <li>After playing <i>Keleres Rider</i>, a player may use additional abilities, including playing other rider cards.</li>
    <ol><li>Any other riders may be played to predict the same or a different outcome.</li></ol>
    <li>The effect of a successful <i>Keleres Rider</i> will be resolved after the effect of the agenda.</li>
    <li>A player who cannot vote may still play a <i>Keleres Rider</i>.</li>
    <li>A player who cannot play action cards may still play a <i>Keleres Rider</i>.</li>
    </ol>

<h1>Agency Supply Network <sub>(Technology)</sub></h1>
    <ol class="note">
    <li>Any abilities that trigger &ldquo;when/after a unit uses <sc>Production</sc>&rdquo; will trigger an additional time, for example, the <i>Sarween Tools</i> technology.</li>
    <li>The additional <sc>Production</sc> ability cannot belong to the same unit as the original <sc>Production</sc> ability.</li>
    <ol>
        <li>During a tactical action, the active player must use every <sc>Production</sc> ability of their units in the active system, or none of them. As such, the additional <sc>Production</sc> ability must belong to a unit in a different system.</li>
        <li>When the Keleres player resolves the secondary ability of the Warfare strategy card, they use the <sc>Production</sc> ability of a single space dock in their home system. As such, they may use the <sc>Production</sc> ability of a second unit in their home system, if present.</li>
    </ol>
    <li>The Keleres player may use Custodia Vigilia as the additional <sc>Production</sc> ability, and when they use Custodia Vigilia as the original <sc>Production</sc> ability, they may use an additional <sc>Production</sc> ability.</li>
    <li>The original <sc>Production</sc> ability must be fully resolved, before the Keleres player may use an additional <sc>Production</sc> ability.</li>
    <ol><li>The Keleres player cannot pay one resource for two fighters or infantry, then place one with a unit using the original <sc>Production</sc> ability and one with the unit using the additional <sc>Production</sc> ability.</li></ol>
    </ol>

<h1>Executive Order <sub>(Technology)</sub></h1>
    <ol class="note">
    <li>If the Keleres player draws one of the <i>Classified Document Leaks</i>, <i>Judicial Abolishment</i>, <i>Miscount Disclosed</i> or <i>New Constitution</i> agendas, they will discard it if required and reveal a new agenda from the deck.</li>
    <li>If the <i>Miscount Disclosed</i> agenda is resolved, the players will vote on the elected agenda too.</li>
    <li>If the <i>Public Execution</i> agenda is resolved, the elected player may still vote in the agenda phase following the current action phase.</li>
    <li>If an agenda would give multiple players a victory point, those players gain that victory point in initiative order, regardless of the Keleres player&rsquo;s initiative.</li>
    <li>Other players may still perform one transaction total with the Keleres player during this turn, if they are neighbors.</li>
    <li>Players exhaust planets to cast votes when resolving <i>Executive Order</i>, even if the <i>Representative Government</i> law is in play.</li>
    <li>If a third law is put into play, the <i>Dictate Policy</i> objective cannot be scored until the agenda phase.</li>
    <li>The <i>Drive the Debate</i> objective cannot be scored while resolving <i>Executive Order</i>.</li>
    <li>Effects from agenda outcomes that occur &ldquo;at the end of this agenda phase&rdquo; are ignored.</li>
    <li>Effects from agenda outcomes that occur &ldquo;at the start of the next strategy phase&rdquo; will still occur.</li>
    </ol>

