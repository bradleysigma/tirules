<p>There currently does not exist a Living Rules Reference section regarding breakthroughs.</p>

<h1>Notes</h1>
    <ol class="note">
    <li>A breakthrough is a faction specific card. Players start the game without their faction&rsquo;s breakthrough.</li>
    <li>A breakthrough is commonly gained through an expedition, or the Thunder&rsquo;s Edge legendary planet ability card. There are other game effects that can gain a player their faction&rsquo;s breakthrough.</li>
    <li>When a player gains their faction&rsquo;s breakthrough, they perform a breakthrough roll, which may cause The Fracture to enter play.</li>
    <li>A breakthrough has 1 or more abilities, and a synergy (other than for the Nekro player).</li>
    <ol>
        <li>If a breakthrough has a passive ability and an exhaust ability, the passive ability always remains in effect, even when the breakthrough is exhausted.</li>
        <li>If a breakthrough has an exhaust ability, the synergy ability always remains in effect, even when the breakthrough is exhausted.</li>
    </ol>
    <li>A breakthrough itself is not a technology; it alone cannot be used to meet a prerequisite when researching a technology, nor can it alone be used to meet the technology requirement of an objective.</li>
    <li>For notes about a breakthrough of a specific faction, see that faction&rsquo;s page.</li>
    </ol>

