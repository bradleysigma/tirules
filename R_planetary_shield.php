<p>Units cannot use the <sc>Bombardment</sc> ability against a planet that contains a unit that has the <sc>Planetary Shield</sc> ability.</p>
    <ol class="lrr">
    <li>The <sc>Planetary Shield</sc> ability does not prevent a planet from being affected by the <i>X&ndash;89 Bacterial Weapon</i> technology.</li>
    <li>The <sc>Planetary Shield</sc> ability prevents an L1Z1X player from using their <sc>Harrow</sc> faction ability.</li>
    <li>If a war sun is in a system with any number of other players&rsquo; units that have the <sc>Planetary Shield</sc> ability, those units are treated as if they do not have that ability.</li>
    <ol>
        <li>Units treated as if they do not have a <sc>Planetary Shield</sc> ability cannot use the <i>Magen Defense Grid</i> technology.</li>
        <li>A war sun can use its <sc>Bombardment</sc> ability against planets that contain units that have the <sc>Planetary Shield</sc> ability.</li>
    </ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A unit with the <sc>Planetary Shield</sc> ability on a planet does not stop of the use of a unit&rsquo;s <sc>Bombardment</sc> ability on other planets in the same system.</li>
    <li>If the <sc>Planetary Shield</sc> ability prevents the use of the  the use of a <sc>Bombardment</sc> ability, then the abilities of the <i>X&ndash;89 Bacterial Weapon &Omega;</i> or <i>X&ndash;89 Bacterial Weapon &Omega;&Omega;</i> technologies cannot be used.</li>
    <li>If a war sun causes a unit to be treated as if it does not have a <sc>Planetary Shield</sc> ability, that unit may still use the ability of the <i>Magen Defense Grid &Omega;</i> or <i>Magen Defense Grid &Omega;&Omega;</i> technologies.</li>
    <li>If a war sun causes a unit to be treated as if it does not have a <sc>Planetary Shield</sc> ability, all units in that system with a <sc>Bombardment</sc> ability may use it against the planet that unit is on.</li>
    </ol>

