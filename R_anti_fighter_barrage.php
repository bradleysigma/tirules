<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '10.' counter(item, decimal);} </style>
<p>A unit with the <sc>Anti&ndash;Fighter Barrage</sc> ability may be able to destroy an opponent&rsquo;s fighters at the onset of a space battle. During the <b>Anti&ndash;Fighter Barrage</b> step of the first round of space combat, players perform the following steps:</p>
    <ol class="lrr">
    <li><b>Step 1</b>: Each player rolls dice for each of their units in the combat that has the <sc>Anti&ndash;Fighter Barrage</sc> ability; this is called an anti&ndash;fighter barrage roll. A hit is produced for each die roll that is equal to or greater than the unit&rsquo;s <sc>Anti&ndash;Fighter Barrage</sc> value.</li>
    <ol>
        <li>A unit&rsquo;s <sc>Anti&ndash;Fighter Barrage</sc> ability is presented along with a unit&rsquo;s attributes on faction sheets and unit upgrade technology cards.</li>
        <li>The <sc>Anti&ndash;Fighter Barrage</sc> ability is displayed as <sc>Anti&ndash;Fighter Barrage X (&times;Y).</sc> The X is the minimum value needed for a die to produce a hit, and Y is the number of dice rolled.</li>
        <li>Game effects that reroll, modify, or otherwise affect combat rolls do not affect anti&ndash;fighter barrage rolls.</li>
        <li>This ability can still be used if no fighters are present; hits produced may be used to trigger specific abilities.</li>
    </ol>
    <li><b>Step 2</b>: Each player must choose and destroy one of their fighters in the active system for each hit their opponent&rsquo;s anti&ndash;fighter barrage roll produced.</li>
    <ol><li>If a player has to assign more hits than they have fighters in the active system, the excess hits have no effect.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A non&ndash;fighter ship cannot use its <sc>Sustain Damage</sc> ability to cancel a hit produced by an anti&ndash;fighter barrage roll.</li>
    <li>Effects such as the <i>Shields Holding</i> action card, which trigger on hits produced during space combat, may be used to cancel a hit produced by an anti&ndash;fighter barrage roll.</li>
    <li>The &ldquo;0&rdquo; side of the d10 represents a result of 10.</li>
    </ol>

