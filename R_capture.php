<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '17.' counter(item, decimal);} </style>
<p>Some abilities instruct a player to capture a unit, preventing the unit&rsquo;s original owner from using it.</p>
    <ol class="lrr">
    <li>If a player captures a non&ndash;fighter ship or mech, they place it on their faction sheet. When such a unit is returned, it is placed into the reinforcements of the original owner.</li>
    <li>A captured non&ndash;fighter ship or mech is returned under the following circumstances:</li>
    <ol>
        <li>If the player who captured the unit agrees to return it as part of a transaction.</li>
        <li>If an ability instructs the capturing player to return the unit as part of an ability&rsquo;s cost.</li>
        <li>If the player whose unit was captured blockades a space dock of the player who captured the unit.</li>
    </ol>
    <li>If a player captures a fighter or infantry, it is placed in its reinforcements instead of on the capturing player&rsquo;s faction sheet; the capturing player places a fighter or infantry token from the supply on their faction sheet instead.</li>
    <ol>
        <li>Captured fighters and infantry do not belong to any player and are returned only when an ability instructs the capturing player to do so.</li>
        <li>Captured fighters and infantry cannot be returned as part of a transaction.</li>
        <li>Captured fighters and infantry are not returned as the result of a blockade.</li>
    </ol>
    <li>When a captured fighter or infantry is returned, it is placed in the supply.</li>
    <li>While a unit is captured, it cannot be produced or placed by its original owner until it is returned.</li>
    <li>If one or more of a player&rsquo;s space docks is being blockaded, that player cannot capture units from the blockading players.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>For more information, see the <a href="/F_vuilraith">Vuil&rsquo;raith faction notes page</a>.</li>
    <li>A captured unit may only be returned to the player that originally owned it, and not to another player.</li>
    <li>The Nekro player and the Yssaril player are able to capture units by copying the Vuil&rsquo;raith player&rsquo;s abilities, but they will be unable to spend the captured units.</li>
    <li>If a player is eliminated, any of their units that are captured remain with the player that captured them. When the capturing player would return them, the units are instead returned to the game box.</li>
    </ol>

