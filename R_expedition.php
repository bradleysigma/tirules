<p>There currently does not exist a Living Rules Reference section regarding expeditions.</p>

<h1>Notes</h1>
    <ol class="note">
    <li>A player may perform an expedition, which will unlock their breakthrough, and potentially put The Fracture and/or Thunder&rsquo;s Edge into play.</li>
    <li>At the end of their turn, a player may perform an expedition. To do so they choose one of the unclaimed slices on the expedition token. They pay the indicated cost, and place one of their control tokens in the indicated area to claim it.</li>
    <li>When a player performs an expedition, they gain their faction&rsquo;s breakthrough if they do not already have it. This will trigger a breakthrough roll to possibly place The Fracture into play.</li>
    <li>The costs to perform an expedition are, in clockwise order from the top of the expedition token:</li>
    <ol>
        <li>Spend five resources;</li>
        <li>Discard two action cards;</li>
        <li>Spend five influence;</li>
        <li>Discard one unscored secret objective;</li>
        <li>Exhaust a planet that has a technology speciality; or</li>
        <li>Spend three trade goods.</li>
    </ol>
    <li>One player may claim multiple slices. A single slice cannot be claimed multiple times (neither by the same or different players).</li>
    <ol><li>If a player already has their breakthrough when they claim a slice, no additional effect will trigger.</li></ol>
    <li>Once the sixth and final slice has been claimed, Thunder&rsquo;s Edge will enter play.</li>
    <ol>
        <li>First, all command tokens on the expedition token are returned to the respective player&rsquo;s reinforcements, noting which player(s) had the most claimed slices.</li>
        <li>Then, the player who claimed the sixth and final slice flips the expedition token to the Thunder&rsquo;s Edge side, and places it in a system on the game board. They must choose a system that does not contain a planet, a supernova, or a printed wormhole, and is not in The Fracture, should it be in play.</li>
        <li>Finally, the player who had claimed the most slices of the expedition token places a number of infantry equal to the number of slices they had claimed on to Thunder&rsquo;s Edge. If multiple players are tied for the most number of slices, the player placing Thunder&rsquo;s Edge chooses one of those players to place the infantry; they may choose themself. The player placing the infantry then gains control of Thunder&rsquo;s Edge.</li>
        <li>If Thunder&rsquo;s Edge is place in a system containing a frontier token, that token is not removed. It may be explored by a player with the <i>Dark Energy Tap</i> technology at a later point in time.</li>
    </ol>
    <li>If a player has not gained their breakthrough by the time Thunder&rsquo;s Edge is placed, they will be unable to gain their breakthrough by performing an expedition.</li>
    </ol>

