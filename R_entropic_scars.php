<h1>Notes</h1>
    <ol class="note">
    <li>An entropic scar is an anomaly that affects unit abilities and faction technologies.</li>
    <li>Unit in an entropic scar cannot use unit abilities.</li>
    <li>Unit outside of an entropic scar cannot use unit abilities against units that are in an entropic scar.</li>
    <li>Wormhole tokens that would be placed in an entropic scar are returned to the supply or a player&rsquo;s reinforcements as appropriate.</li>
    <li>At the start of the status phase, a player with ships in an entropic scar may spend a commend token from their strategy pool to gain one of their faction&ndash;specific technologies.</li>
    <ol>
        <li>A player need not meet the prerequisites for the technology they gain this way.</li>
        <li>Ground forces in an entropic scar do now allow a player to gain a faction technology this way.</li>
        <li>If a player has ships in two (or more) entropic scars, they may spend two command tokens from their strategy pool at the start of the status phase to gain both of their faction&ndash;specific technologies.</li>
    </ol>
    </ol>

