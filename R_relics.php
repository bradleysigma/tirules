<p>Relics are powerful artifacts with unique abilities.</p>
    <ol class="lrr">
    <li>Players can use the abilities of hazardous, cultural, and industrial relic fragments in their play area to draw cards from the relic deck.</li>
    <ol><li>Relic fragments can be found when exploring planets and frontier tokens, and can be exchanged with other players as part of transactions.</li></ol>
    <li>When a player is instructed to gain a relic, they draw the top card of the relic deck and place it faceup in their play area.</li>
    <ol><li>If there are no cards in the relic deck, they do not gain a relic.</li></ol>
    <li>A player can use the abilities of relics that are in their play area.</li>
    <li>Relics cannot be traded.</li>
    </ol>

