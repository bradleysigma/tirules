<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '94.' counter(item, decimal);} </style>
<p>A transaction is a way for a player to exchange commodities, trade goods, promissory notes, and relic fragments.</p>
    <ol class="lrr">
    <li>During the active player&rsquo;s turn, they may resolve up to one transaction with each of their neighbors.</li>
    <ol><li>A player can resolve a transaction at any time during their turn, even during a combat.</li></ol>
    <li>To resolve a transaction, a player gives any number of trade goods and commodities and up to one promissory note to a neighbor in exchange for any number of trade goods, commodities, and relic fragments, and up to one promissory note.</li>
    <li>Players can exchange commodities, trade goods, promissory notes, and relic fragments, but cannot exchange other types of cards or tokens.</li>
    <ol><li>The Emirates of Hacan can also exchange action cards with other players as part of their transactions.</li></ol>
    <li>A transaction does not have to be even. A player may exchange components of unequal value or give components without receiving something in return.</li>
    <ol><li>The players agree on the terms of the transaction before exchanging any components. After the components are traded, the transaction cannot be undone.</li></ol>
    <li>Players can resolve a transaction as part of a deal.</li>
    <li>While resolving <b>each</b> agenda during the agenda phase, a player may perform one transaction with each other player.</li>
    <ol><li>Players do not need to be neighbors to perform these transactions.</li></ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>Relic fragments may be traded. Relics themselves cannot be.</li>
    <li>Captured units may be returned to the player that originally owned them as part of a transaction.</li>
    <li>If a player becomes neighbors with another player on their turn, they may then perform a transaction with that player.</li>
    <ol>
        <li>If a player stops being neighbors with another player that they have performed a transaction with this turn, but then become neighbors with that player again later that same turn, they cannot perform a second transaction with that player.</li>
        <li>A player may perform a transaction during combat with their opponent, even if they are neighbors only in the active system.</li>
    </ol>
    <li>If a player may perform multiple actions on their turn, they may still only resolve one transaction with each of their neighbors.</li>
    <li>If an agenda is discarded and replaced with another, players who resolved a transaction during the discarded agenda may resolve another transaction during the replacement agenda.</li>
    </ol>

