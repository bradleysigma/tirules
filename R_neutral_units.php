<h1>Rules Reference</h1>
<p>There currently does not exist a Living Rules Reference section regarding neutral units.</p>

<h1>Notes</h1>
    <ol class="note">
    <li>Neutral units are placed in some system within The Fracture when it enters play. Several other game effects also place neutral units.</li>
    <li>If a player has ships in a space area that contains neutral units, they will resolve a space combat against those units.</li>
    <li>If a player has ground forces on a planet that contains neutral units, they will resolve a ground combat against those units.</li>
    <li>Any player other than the active player may roll the combat and unit ability dice during a combat involving neutral units. The combat values and unit abilities are found on the neutral unit reference card.</li>
    <li>Neutral units will always use each and every unit ability that they can.</li>
    <li>Neutral units do not take turns, own technology, or draw action cards, and so on. They cannot retreat from combat.</li>
    <li>When a hit is assigned against a group of neutral units, it is assigned to the unit that is lowest on the neutral unit reference card; generally, this corresponds to the unit with the lowest cost.</li>
    <ol><li>Hits produced against neutral units may only be assigned to eligible unit types, as usual. For example, <sc>Anti&ndash;Fighter Barrage</sc> hits produced against neutral units may only be assigned to neutral fighters.</li></ol>
    <li>Neutral units are considered to be another player&rsquo;s ships for abilities and other game effects. However, there is no neutral player.</li>
    </ol>

