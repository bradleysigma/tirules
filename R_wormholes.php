<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '101.' counter(item, decimal);} </style>
<p>Some systems contain wormholes. Systems that contain identical wormholes are adjacent.</p>
    <ol class="lrr">
    <li>There are two basic types of wormholes: alpha and beta.</li>
    <li>If a player has a PDS unit upgrade technology, they can use the <sc>Space Cannon</sc> abilities of their PDS units through wormholes.</li>
    <li>Players can be neighbors and perform transactions through wormholes.</li>
    <li>There are two advanced types of wormhole: delta and gamma. These wormholes follow all other wormhole rules.</li>
    <ol>
        <li>The delta wormholes are present on the Creuss Gate system tile and the Ghosts of Creuss home system tile.</li>
        <li>The gamma wormholes are present on the wormhole nexus and can be discovered during exploration.</li>
    </ol>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>A system with a wormhole is not an anomaly.</li>
    <li>If a system contains multiple wormholes of the same type, those are treated as separate wormholes.</li>
    <li>A system cannot be adjacent to itself, even if it has multiple wormholes in it.</li>
    <li>A player chooses the path of adjacent systems each ship moves through during movement. If two systems on that are adjacent because of a wormhole, that ship moves through that wormhole.</li>
    <ol>
        <li>If the two systems are adjacent because of a wormhole and because of a shared edge or another reason, the player may choose whether or not the ship moves through the wormhole.</li>
        <li>If the two systems are adjacent because of two types of wormholes, the player chooses which type of wormhole the ship moves through.</li>
        <li>A player may move a ship back and forth through a wormhole between two systems any number of times, if its movement value is high enough.</li>
    </ol>
    <li>For more information on how the Creuss interact with wormholes, see <a href="/F_creuss">their faction notes page</a>.</li>
    </ol>

