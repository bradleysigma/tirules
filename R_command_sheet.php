<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '19.' counter(item, decimal);} </style>
<p>Each player has a command sheet that contains a strategy pool, a tactic pool, a fleet pool, a trade good area, and a quick reference.</p>
    <ol class="lrr">
    <li>The pools on the command sheet are where players place their command tokens. Command tokens in a player&rsquo;s pools are used by that player to perform strategic and tactical actions and to increase the number of ships that player can have in each system.</li>
    <li>The trade good area on the command sheet is where a player places their trade good tokens; trade tokens in a player&rsquo;s trade good area can be spent by that player as resources, influence, or to resolve certain game effects that require trade goods.</li>
    <li>Players who are familiar with the game can hide the quick reference by placing that portion of the command sheet under their faction sheets.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>When a command token is gained, it may be placed in any of the three pools if no specific pool is specified by the gaining effect.</li>
    </ol>

