<h1>Rules Reference</h1>
<style> .lrr > li:before, .lrr > h2 > li:before {content: '3.' counter(item, decimal);} </style>
<p>During the action phase, each player takes a turn in initiative order. During a player&rsquo;s turn, they perform a single action. After each player has taken a turn, player turns begin again in initiative order. This process continues until all players have passed.</p>
    <ol class="lrr">
    <li>During a player&rsquo;s turn, they may perform one of the following three types of actions: a strategic action, a tactical action, or a component action.</li>
    <li>If a player cannot perform an action, they must pass.</li>
    <li>After a player has passed, they have no further turns and cannot perform additional actions during that action phase.</li>
    <ol>
        <li>During a turn that a player passes, they can resolve transactions and &ldquo;at the start of your turn&rdquo; abilities.</li>
        <li>A player that has passed can still resolve the secondary ability of other players&rsquo; strategy cards.</li>
        <li>It is possible for a player to perform multiple, consecutive actions during an action phase if all other players have passed during that action phase.</li>
    </ol>
    <li>A player cannot pass until they have performed the strategic action of their strategy card.</li>
    <ol><li>During a three&ndash;player or four&ndash;player game, a player cannot pass until they have exhausted both of their strategy cards.</li></ol>
    <li>After all players have passed, play proceeds to the status phase.</li>
    </ol>

<h1>Notes</h1>
    <ol class="note">
    <li>Players may still play non&ndash;component action action cards after they have passed.</li>
    <li>Any abilities that occur at the end of an action happen before any abilities that occur at the end of a player&rsquo;s turn.</li>
    <li>A player may resolve &ldquo;at the start of your turn&rdquo; and &ldquo;at the end of your turn&rdquo; abilities on the turn that they pass.</li>
    <li>If all other players have passed, a player will continue to resolve &ldquo;at the start of your turn&rdquo; and &ldquo;at the end of your turn&rdquo; abilities.</li>
    </ol>

